package Basics;

/**
 * var
 */
public class Variables {
public static void main(String[] args) {
    int a = 10;
    int _a = 10;
    // int ?a = 10;//not allowed
    // int 0a = 10;//not allowed
    int a$ = 10;//allowed
    int $ =1;//allowed 
    // int ?  = 1;//not allowed
    System.out.println(a);
    System.out.println(_a);
    System.out.println(a$);
    System.out.println($);
    // single line comment by "//"
    // multi line comment by "/* */"
}
}